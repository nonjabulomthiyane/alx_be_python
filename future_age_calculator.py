# Receive user input for current age
current_age = int(input("How old are you? "))

# Constants for current and future years
future_year = 2050
current_year = 2023

# Calculate age in the future year
age_in_2050 = current_age + (future_year - current_year)

# Print the result
print(f"In {future_year}, you will be {age_in_2050} years old.")
